	<h1 class="text-center">Tentang Pathfinder</h1>
	<div class="my-2">
		<p>
			Melakukan penelitian merupakan aktivitas wajib yang harus dikuasai oleh semua sivitas
			akademika di perguruan tinggi. Dosen atau mahasiswa dan bahkan tendik (fungsional tertentu),
			saat ini dituntut mampu melakukan penelitian dan menghasilkan publikasi. Dalam proses
			penelitian, akan selalu ada tahap pencarian topik penelitian, penelusuran literatur untuk
			memperkuat latar belakang dan pembahasan atau pun analisis.
		</p>
		<p>
			Dalam proses penelusuran, selain diperlukan skills yang memadai, juga diperlukan adanya media
			penelusuran yang baik. Isi yang lengkap, menyeluruh dan terpadu (saling ketersinambungan satu
			dengan yang lain) dan yang lebih penting adalah terjamin validitas sumbernya. Salah satu media
			penelusuran yang sudah dikenal adalah Pathfinder. Pathfinder membantu penelusuran menjadi
			lebih efektif karena subjek dan bentuk sumber informasinya sudah dikelompokkan sedemikian rupa
			dan sudah melalui kurasi data yang sedemikian, sehingga sumber informasi dan konten di
			dalamnya valid dan bisa dikutip secara aman untuk kepentingan akademik.
		</p>
		<p>
			Inovasi ini adalah LANTERA: UNPAD WEB-BASED PATHFINDER. Aplikasi ini merupakan aplikasi
			berbasis web yang diperuntukkan bagi civitas akademika Unpad dan masyarakat umum juga dengan
			beberapa ketentuan khusus. Aplikasi ini disusun berdasarkan subjek keilmuan yang ada di Unpad.
			Aplikasi ini dikerjakan oleh para pustakawan subjek di tiap fakultas dengan proses verifikasi
			oleh tim Knowledge Management (KM) di Pusat Pengelolaan Pengetahuan. LANTERA berisi sumber
			informasi valid, terpercaya dan bisa langsung digunakan (baca: dikutip) untuk keperluan
			ilmiah. Hal ini disebabkan semua cantuman sudah melalui verifikasi dan proses kurasi.
		</p>
		<p>
			Inovasi ini terlaksana atas dukungan Direktorat Inovasi dan Korporasi dan Direktorat
			Pendidikan dan Internasionalisasi melalui Program Hibah Inovasi dan Kreasi Tenaga Kependidikan
			Universitas Padjadjaran (HIKTU) Tahun 2023. Founder aplikasi ini adalah tim di divisi
			Knowledge Management Pusat Pengelolaan Pengetahuan. UNPAD WEB-BASED PATHFINDER akan terus
			mengalami pembaruan seiring perkembangan UNPAD. Dalam rangka mendukung UNPAD sebagai 500
			perguruan tinggi terbaik dunia dan sebagai hybrid university, adanya sumberdaya informasi
			terseleksi adalah suatu keniscayaan.
		</p>
		<p>
			LANTERA adalah harapan baru yang bisa menerangi dan menunjukkan arah bagi peningkatan
			efektifitas penelusuran informasi untuk keperluan riset mahasiswa dan dosen Unpad. Lebih jauh
			lagi, LANTERA akan memperlancar laju Unpad menuju universitas terbaik 500 dunia. Selain itu
			menjadi layanan yang selaras dengan UNPAD sebagai hybrid university.
		</p>
